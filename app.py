import streamlit as st
from google.oauth2 import service_account
from google.cloud import discoveryengine_v1alpha as discoveryengine

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 1. Cargar secretos y credenciales de GCP
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
try:
    gcp_creds_dict   = st.secrets["gcp"]["credentials"]
    PROJECT_NUMBER   = st.secrets["gcp"]["project_number"]   # 107344050799
    DATA_STORE_ID    = st.secrets["gcp"]["data_store_id"]    # casos-mvp_1752189050616
except KeyError as e:
    st.error(f"Falta la clave {e} en .streamlit/secrets.toml")
    st.stop()

credentials   = service_account.Credentials.from_service_account_info(gcp_creds_dict)
LOCATION      = "global"    # El endpoint de b√∫squeda siempre usa global

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 2. Crear cliente y ruta SERVING_CONFIG (una vez)
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
@st.cache_resource(show_spinner=False)
def get_client():
    return discoveryengine.SearchServiceClient(credentials=credentials)

client = get_client()

SERVING_CONFIG = client.serving_config_path(
    project        = PROJECT_NUMBER,
    location       = LOCATION,
    data_store     = DATA_STORE_ID,
    serving_config = "default_search",          # Ver pesta√±a ‚ÄúServing configs‚Äù en consola
)

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 3. L√≥gica de b√∫squeda
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
def search_cases(query: str):
    request = discoveryengine.SearchRequest(
        serving_config = SERVING_CONFIG,
        query          = query,
        page_size      = 10,
        content_search_spec = {
            "summary_spec": {
                "summary_result_count": 3,
                "model_prompt_spec": {
                    "preamble": (
                        "Resume el siguiente texto en espa√±ol, con detalle, "
                        "y tono de asistente legal."
                    )
                },
            }
        },
    )
    return client.search(request)

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# 4. Interfaz Streamlit
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
st.title("‚öñÔ∏è Buscador Legal IA")

st.write(
    "Haz una pregunta sobre los casos cargados. "
    "Obtendr√°s un resumen generado por IA y los documentos relevantes."
)

query = st.text_input(
    label       = "Escribe tu pregunta:",
    placeholder = "Ej: ¬øQu√© se resolvi√≥ en el caso de Equipos M√©dicos Peninsulares?"
)

# üëâ  Bot√≥n creado UNA sola vez
search_pressed = st.button("Buscar", key="search_btn")

if search_pressed and query:
    with st.spinner("Buscando‚Ä¶"):
        try:
            response = search_cases(query)

            # ‚îÄ‚îÄ Resumen ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
            summary_text = getattr(response.summary, "summary_text", "")
            if summary_text:
                st.subheader("Resumen:")
                st.markdown(summary_text)
            else:
                st.info("No se pudo generar un resumen para esta consulta.")

            # ‚îÄ‚îÄ Resultados ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
            if response.results:
                st.subheader("Documentos relevantes:")
                for result in response.results:
                    meta  = result.document.derived_struct_data
                    title = meta.get("title", "T√≠tulo no disponible")
                    link  = meta.get("link", "#")

                    with st.expander(title):
                        for snip in meta.get("snippets", []):
                            st.markdown(f"> ‚Ä¶{snip.get('snippet', '')}‚Ä¶")
                        if link != "#":
                            st.markdown(f"_[Abrir documento completo]({link})_")
            else:
                st.warning("No se encontraron documentos para esa b√∫squeda.")

        except Exception as e:
            st.error(f"Ocurri√≥ un error al contactar la API:\n\n{e}")

elif search_pressed:
    st.warning("Por favor, escribe una pregunta antes de buscar.")
